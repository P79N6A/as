public class ProvinceShortBean {

    /**
     * codes : 1001
     * horizontalGap : 1.85%p
     * keyEdgeFlags : left
     * keyLabel : 京
     * keyWidth : 8%p
     */

    private String codes;
    private String horizontalGap;
    private String keyEdgeFlags;
    private String keyLabel;
    private String keyWidth;

    public String getCodes() {
        return codes;
    }

    public void setCodes(String codes) {
        this.codes = codes;
    }

    public String getHorizontalGap() {
        return horizontalGap;
    }

    public void setHorizontalGap(String horizontalGap) {
        this.horizontalGap = horizontalGap;
    }

    public String getKeyEdgeFlags() {
        return keyEdgeFlags;
    }

    public void setKeyEdgeFlags(String keyEdgeFlags) {
        this.keyEdgeFlags = keyEdgeFlags;
    }

    public String getKeyLabel() {
        return keyLabel;
    }

    public void setKeyLabel(String keyLabel) {
        this.keyLabel = keyLabel;
    }

    public String getKeyWidth() {
        return keyWidth;
    }

    public void setKeyWidth(String keyWidth) {
        this.keyWidth = keyWidth;
    }
}
