package com.yaoguang.greendao.entity.driver;

/**
 * 未读信息的条数
 */
public class UnreadNum {

    /**
     * orderMsgNumber : 2
     * sysMsgNumber : 94
     */

    private int orderMsgNumber;
    private int sysMsgNumber;

    public int getOrderMsgNumber() {
        return orderMsgNumber;
    }

    public void setOrderMsgNumber(int orderMsgNumber) {
        this.orderMsgNumber = orderMsgNumber;
    }

    public int getSysMsgNumber() {
        return sysMsgNumber;
    }

    public void setSysMsgNumber(int sysMsgNumber) {
        this.sysMsgNumber = sysMsgNumber;
    }
}
